#For Loop
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)


for i in range(10):
    if i == 5:
        break 
    print(i)

for i in range(5):
    if i == 2:
        continue
    print(i)

#While Loop
count = 0
while count < 5:
    print(count)
    count += 1

